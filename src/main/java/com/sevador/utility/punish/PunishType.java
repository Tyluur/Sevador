package com.sevador.utility.punish;

/**
 * The punishment types.
 * @author Emperor
 *
 */
public enum PunishType {

	/**
	 * The jail punishment - stops the user from doing anything content related.
	 */
	JAIL,
	
	/**
	 * The mute punishment - disables any form of written interaction.
	 */
	MUTE,
	
	/**
	 * The ban punishment - disables the account from logging in.
	 */
	BAN,
}