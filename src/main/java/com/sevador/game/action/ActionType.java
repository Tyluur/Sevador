package com.sevador.game.action;

/**
 * Represents the possible action types.
 * @author Emperor
 *
 */
public enum ActionType {

	/**
	 * The default action type.
	 */
	DEFAULT,
	
	/**
	 * Represents a movement action.
	 */
	MOVEMENT,
	
	/**
	 * Represents a combat action.
	 */
	COMBAT,
	
	/**
	 * Represents a skill action.
	 */
	SKILL,
	
}